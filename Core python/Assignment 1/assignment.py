###Assignment_operators
a=5
b=3

# = (Equal to)

# += (Plus equal to)
a+=5
print(a)

# -= (Minus Equal to)
a-=b
print(a)

# *= (Multiply Equal to)
a*=b
print(a)

# /= (Division equal to)
a/=b
print(a)

# //= (Floor division equal to)
a//=b
print(a)

# %= (Modulus Equal to)
a%=b
print(a)

# **= (Exponantial Equal to)
a**=b
print(a)