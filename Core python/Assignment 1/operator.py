####Arithmatic_operators
x=10
y=5
# + (Addition)
print(x+y)

# - (Substraction)
print(x-y)

# * (Multiplication)
print(x*y)

# / (Division)
print(x/y)

# // (Floor division)
print(x//y)

# % (Modulus)
print(x%y)

# ** (Exponantial)
print(x**y)

